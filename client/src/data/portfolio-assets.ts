// ============================================================
// UTILITY FUNCTIONS
// ============================================================

/**
 * Get all hero images for scrolling animation
 */
export const getHeroImages = () => portfolioImages.hero;

/**
 * Get client logo paths
 */
export const getClientLogoPaths = () => clientLogos.work.map(logo => logo.path);

/**
 * Get client logo by name
 */
export const getClientLogoByName = (name: string) => {
  return clientLogos.work.find(logo => logo.name.toLowerCase() === name.toLowerCase());
};

/**
 * Get video asset by section
 */
export const getVideoAsset = (section: 'newEra' | 'successStories') => {
  return videoAssets[section];
};

/**
 * Get section image by name
 */
export const getSectionImage = (section: keyof typeof sectionImages, index?: number) => {
  const images = sectionImages[section];
  if (Array.isArray(images) && typeof index === 'number') {
    return images[index];
  }
  return images;
};

